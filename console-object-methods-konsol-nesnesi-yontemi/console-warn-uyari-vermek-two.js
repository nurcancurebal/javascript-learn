// Tarayıcıda uyarı vermek için console.warn() kullanıyoruz. Örneğin, bir paketin sürümünün veya kötü uygulamaların kullanımdan
// kaldırıldığını bildirmek veya uyarmak için. Uyarı mesajlarını görmek için aşağıdaki kodu kopyalayın ve tarayıcı konsoluna yapıştırın.

console.warn('This is a warning'); // sari bir üçgen içerisinde beyaz ünlem işareti yanında sarı yazı ile This is a warning yazar

console.warn(
    'You are using React. Do not touch the DOM. Virtual DOM will take care of handling the DOM!'
);  // sari bir üçgen içerisinde beyaz ünlem işareti yanında sarı yazı ile You are using React. Do not touch the DOM. Virtual DOM will take care of handling the DOM! yazar

console.warn('Warning is different from error');
// sari bir üçgen içerisinde beyaz ünlem işareti yanında sarı yazı ile Warning is different from error yazar